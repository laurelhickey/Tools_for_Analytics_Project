
CREATE TABLE IF NOT EXISTS hourly_weather
    (id INTEGER PRIMARY KEY AUTOINCREMENT,
    DATE DATE,
    LATITUDE INTEGER,
    LONGITUDE INTEGER,
    NAME TEXT,
    hours INTEGER,
    newDATE DATE)

CREATE TABLE IF NOT EXISTS daily_weather
    (id INTEGER PRIMARY KEY AUTOINCREMENT,
    DATE DATE,
    LATITUDE INTEGER,
    LONGITUDE INTEGER,
    NAME TEXT,
    days INTEGER,
    newDATE DATE)
